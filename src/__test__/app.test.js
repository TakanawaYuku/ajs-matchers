import sortPlayers from '../app';

const currentData = [
  { name: 'мечник', health: 10 },
  { name: 'маг', health: 100 },
  { name: 'лучник', health: 80 },
];

const expectedData = [
  { name: 'маг', health: 100 },
  { name: 'лучник', health: 80 },
  { name: 'мечник', health: 10 },
];

test('sort players', () => {
  const result = sortPlayers(currentData);
  expect(result).toEqual(expectedData);
});
